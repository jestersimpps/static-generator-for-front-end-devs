////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//get/set locale on load
////////////////////////////////////////////////////////////////////////////////////////////////////////////////

$(function() {
  $('#' + getLocale() + 'but').addClass('active');
});

////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//set locale
////////////////////////////////////////////////////////////////////////////////////////////////////////////////

function setLocale(lan) {

  var hostname = window.location.hostname;
  var slug = window.location.pathname.split('/').pop();
  var hash = window.location.hash;
  var port = null;
  if (lan == 'nl') {
    lan = '';
    port = window.location.port;
  } else {
    port = window.location.port + '/';
  }
  window.location = 'http://' + hostname + ':' + port + lan + '/' + slug + hash;

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//get locale
////////////////////////////////////////////////////////////////////////////////////////////////////////////////

function getLocale() {
  var url = window.location.pathname;

  //return fr for french
  if (url.indexOf('fr') > 0) {
    return 'fr';
  }
  //return es for spanish
  if (url.indexOf('es') > 0) {
    return 'es';
  }
  //return default
  return 'en';

}
